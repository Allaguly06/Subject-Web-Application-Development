    <h2>Встраивание PHP в HTML</h2>
    <p>PHP-код может быть встроен непосредственно в HTML-документ с помощью специальных тегов &lt;?php ... ?&gt;. Всё, что находится вне этих тегов, обрабатывается как обычный HTML-код. Это позволяет легко смешивать статический и динамический контент на одной странице. Файлы с PHP-кодом должны иметь расширение .php, чтобы веб-сервер передавал их интерпретатору PHP.</p>
    
    <p>Важно отметить, что PHP-код выполняется на стороне сервера, а клиент (браузер) получает уже готовый HTML-код. Это обеспечивает безопасность, так как пользователь не видит исходный код скриптов. Именно поэтому PHP идеально подходит для создания динамических веб-сайтов с доступом к базам данных, обработкой форм и аутентификацией пользователей.</p>
    
    <h2>Переменные и типы данных</h2>
    <p>В PHP переменные начинаются со знака доллара ($). PHP является языком с динамической типизацией, что означает, что вам не нужно объявлять тип переменной заранее — он определяется автоматически при присваивании значения. Основные типы данных: integer (целые числа), float (числа с плавающей точкой), string (строки), boolean (логический тип), array (массивы), object (объекты) и NULL.</p>
    
