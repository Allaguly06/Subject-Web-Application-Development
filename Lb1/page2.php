    <h2>Условные операторы и циклы</h2>
    <p>PHP поддерживает все стандартные конструкции управления потоком выполнения: if/else, switch, while, do-while, for и foreach. Условный оператор if позволяет выполнять различные блоки кода в зависимости от условий. Циклы используются для многократного выполнения одного и того же блока кода.</p>
    
    <p>Пример использования условного оператора: если текущая секунда чётная или нечётная, можно выводить разный контент. Цикл foreach особенно удобен для перебора элементов массива и часто используется при работе с базами данных для вывода результатов запросов.</p>
    
    <h2>Работа с функциями</h2>
    <p>Функции в PHP — это блоки кода, которые можно вызывать многократно. Они могут принимать параметры и возвращать значения. PHP имеет огромное количество встроенных функций для работы со строками, массивами, файлами, базами данных и другими задачами. Вы также можете создавать собственные функции с помощью ключевого слова function.</p>
    
    <p>Функции помогают избежать дублирования кода, делают программу более структурированной и легкой для поддержки. Хорошая практика — выносить повторяющиеся фрагменты кода в отдельные функции.</p>
